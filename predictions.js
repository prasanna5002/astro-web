const SUBA_NAKSHATRAS = ["அசுவினி", "ரோகிணி", "மிருகசீரிடம்", "புனர்பூசம்", "பூசம்", "மகம்", "உத்திரம்", "அஸ்தம்", "சித்திரை", "சுவாதி", "அனுஷம்", "மூலம்", "உத்திராடம்", "திருவோணம்", "சதயம்", "உத்திரட்டாதி", "ரேவதி"];
const SUBA_WEEKDAYS = ["திங்கட்கிழமை", "புதன்கிழமை", "வியாழக்கிழமை", "வெள்ளிக்கிழமை"];
