  const gradeLabel = g => g === 2 ? '<span style="color:#55ff55;font-weight:700;">உத்தமம்</span>' : g === 1 ? '<span style="color:var(--primary-gold);font-weight:700;">மத்திமம்</span>' : '<span style="color:#ff5555;font-weight:700;">இல்லை</span>';

