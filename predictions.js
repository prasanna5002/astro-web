    <p class="reading-meta" style="margin-bottom: 1rem;">மணமகன்: ${NAKSHATRAS[boyNak].nameTa} ${boyPada}-ம் பாதம் (${RASIS[boyRasi].nameTa}) | மணமகள்: ${NAKSHATRAS[girlNak].nameTa} ${girlPada}-ம் பாதம் (${RASIS[girlRasi].nameTa})</p>
