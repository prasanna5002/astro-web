  // 6. ராசி பொருத்தம் — பெண் ராசியிலிருந்து ஆண் ராசி
  const rasiCount = ((boyRasi - girlRasi + 12) % 12) + 1;
  if ([6, 8].includes(rasiCount)) add("ராசி", 0, "ஆண் ராசி பெண்ணுக்கு 6/8-ல் — ராசிப் பொருத்தம் அதமம்.");
