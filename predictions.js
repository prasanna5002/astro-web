  // 8. வசிய பொருத்தம்
  if ((VASYA_TABLE[girlRasi] || []).includes(boyRasi) || (VASYA_TABLE[boyRasi] || []).includes(girlRasi)) add("வசியம்", 2, "பரஸ்பர ஈர்ப்பும் அன்பும் நிறைந்த இணைவு.");
