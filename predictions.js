function rajjuOf(starNum) {
  return RAJJU_GROUPS.find(g => g.stars.includes(starNum));
}

function yoniRelation(a, b) {
  if (a[0] === b[0]) return "same";
  const enemy = YONI_ENEMIES.some(pair => (pair[0] === a[0] && pair[1] === b[0]) || (pair[1] === a[0] && pair[0] === b[0]));
  return enemy ? "enemy" : "friend";
}

