// பொதுவான வீட்டுப் பலன் உருவாக்கி (புத்திரம்/சகோதரம்/சொத்து)
function genHouseTopicReport(birthData, houseNum, topicTa, karakaKey, goodText, midText, weakText, karakaGood) {
  const lagnaSign = Math.floor(birthData.lagna / 30) % 12;
  const lord = SIGN_LORDS[(lagnaSign + houseNum - 1) % 12];
  const lh = predHouseOf(birthData, lord, lagnaSign);
  const st = getPlanetState(lord, Math.floor(birthData[lord] / 30) % 12);
  const kSt = getPlanetState(karakaKey, Math.floor(birthData[karakaKey] / 30) % 12);
  const kH = predHouseOf(birthData, karakaKey, lagnaSign);

  let text = `${topicTa} குறிக்கும் ${houseNum}-ம் வீட்டின் அதிபதி ${planetNamesTa[lord]}; அவர் ${lh}-ம் வீட்டில் ${STATE_NAMES_TA[st]} உள்ளார். காரகக் கிரகமான ${planetNamesTa[karakaKey]} ${kH}-ம் வீட்டில் ${STATE_NAMES_TA[kSt]} உள்ளார். `;

  const lordStrong = (st === "exalted" || st === "own") || [1, 4, 5, 7, 9, 10, 11].includes(lh);
  const karakaStrong = (kSt === "exalted" || kSt === "own") || karakaGood.includes(kH);

  if (lordStrong && karakaStrong) text += goodText;
  else if (lordStrong || karakaStrong) text += midText;
  else text += weakText;
  return text;
}

